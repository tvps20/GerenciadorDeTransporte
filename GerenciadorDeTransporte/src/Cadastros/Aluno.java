package Cadastros;

public class Aluno 
{
    private String Nome;
    private String Instituicao;

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public String getInstituicao() {
        return Instituicao;
    }

    public void setInstituicao(String Instituicao) {
        this.Instituicao = Instituicao;
    }
}
